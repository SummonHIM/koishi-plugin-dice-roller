import { Random } from "koishi";

export default function chunibyo(): string {
    const ret: string[] = [
        "在月光的神秘裡，我以满天星辰之力，召唤我心中的暗黑使者，翩翩降临，夜色随之泛起幽冥的光辉。",
        "寒风呼啸，我手握魔法之杖，呼唤风暴之力，唤醒沉睡的灵魂，召唤雷霆闪电，在暗黑中刻印我独特的命运符号。",
        "站在古老祭坛前，我高唱古老咒语，激活召唤之门，从虚空中唤来堕落之力，舞动黑翼，注视着深邃的未来。",
        "星空闪耀，我跃入符文阵中，挥动魔剑，唤醒沉睡的魔法兽，它们的眼眸燃烧着古老的魔力，与我一同融入幻想的战场。",
        "魔法水晶散发着奇异的光辉，我轻启唇辰，吟唱禁忌咒文，召唤时空裂缝，穿越次元，带着中二病的魔法力量穿梭于幻想与现实之间。",
    ];
    return ret[Math.abs(Random.int(ret.length, 0))];
}
